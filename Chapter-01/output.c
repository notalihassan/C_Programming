#include <stdio.h>
int main(){

    // Output Cases
    // %d, %f, %c  (format specifier)

    // printf("line .... %d(data type)", variable_name);

    
    // %d for integer
    int age = 18;
    printf("Age is %d", age); 

    // %f for real or float numbers
    float pi = 3.14;
    printf("value of pi is %f", pi);

    // %c for character
    char star = '$';
    printf("hehehe... %c", star);


}